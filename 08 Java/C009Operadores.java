public class C009Operadores {
    public static void main(String[] args) {

        //Pre-incremento
        int i = 1;
        int j = ++i;    //incrementando i, adjudicando el valor de i a j

        System.out.println("i =" + i);
        System.out.println("j =" + j);

        //Post-incremento
        int k = 1;
        int l = k++;    //adjudicando el valor de k a l, incrementando i

        System.out.println("k =" + k);
        System.out.println("l =" + l);

        }
    }

