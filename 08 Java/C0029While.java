public class C0029While {
    public static void main(String[] args) {

        //En este ejemplo, usaremos un bucle while para imprimir los números del 1 al 10 en la consola.

        int i = 1;

        while (i <= 10) {
            System.out.println(i);
            i++;

        /*
        Este bucle while continuará ejecutándose mientras i sea menor o igual a 10.
        Cuando i sea mayor que 10, la condición del bucle while será falsa y el bucle se detendrá.
        */
    }
}}
